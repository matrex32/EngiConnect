package com.engiconnect.type;

/**
 * An enumeration representing the types of email messages.
 * 
 * @author Denis
 */
public enum EmailType {
	
	/**
     * Email type for confirm registration.
     */
	CONFIRM_REGISTRATION,
	
	/**
	 * Email type for reset password
	 */
	RESET_PASSWORD,
	
	/**
	 * Email type for employer notification
	 */
	EMPLOYER_NOTIFICATION,
	
	
	/**
     * Email type for job application.
     */
	JOB_APPLICATION;
}